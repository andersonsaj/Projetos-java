    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        Locale.setDefault(Locale.US);
        pcartao = Double.parseDouble(txtCartao.getText());
        pcheque = Double.parseDouble(txtCheque.getText());
        pdinheiro = Double.parseDouble(txtDinheiro.getText());
        totalVenda = Double.parseDouble(txtTotal.getText());
        try {
            totalPago = pcartao + pdinheiro + pcheque;
            if (totalPago >= totalVenda) {
                troco = totalPago - totalVenda;
                txtTroco.setText(String.valueOf(String.format("%.2f", troco)));
                Venda objv = new Venda();
                objv.setCliente(cliente_id);
                Date agora = new Date();
                objv.setDataVenda(agora);
                objv.setTotalVenda(totalVenda);
                objv.setObs(txtObs.getText());
                VendaDAO daov = new VendaDAO();
                daov.cadastraVenda(objv);

                objv.setId(daov.retornaUltimaVenda());
                for (int i = 0; i < carrinho.getRowCount(); i++) {
                    ItemVenda item = new ItemVenda();
                    int qtd_estoque, qtd_comprada, qtd_atualizada;
                    Produto objp = new Produto();
                    ProdutoDAO daop = new ProdutoDAO();

                    item.setVenda(objv);
                    objp.setId(Long.parseLong(carrinho.getValueAt(i, 0).toString()));
                    item.setProduto(objp);
                    item.setQtd(Integer.parseInt(carrinho.getValueAt(i, 2).toString()));
                    item.setSubtotal(Double.parseDouble(carrinho.getValueAt(i, 4).toString()));

                    qtd_estoque = daop.retornaEstoqueAtual(objp.getId());
                    qtd_comprada = Integer.parseInt(carrinho.getValueAt(i, 2).toString());
                    if (qtd_estoque > qtd_comprada) {
                        qtd_atualizada = qtd_estoque - qtd_comprada;
                        daop.baixaEstoque(objp.getId(), qtd_atualizada);
                        
                         ItemVendaDAO daoItem = new ItemVendaDAO();
                         daoItem.cadastraItem(item);
                    }else{
                        JOptionPane.showMessageDialog(null, "Estoque n√£o tem essa quantidade!!");
                        this.dispose();
                        FrmVenda tela = new FrmVenda();
                        tela.setVisible(true);
                    }
                }
               JOptionPane.showMessageDialog(null, "Venda efetuada com Sucesso!");
            } else {
                JOptionPane.showMessageDialog(null, "Valor menor que o total!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro inesperado");
        }
    }//GEN-LAST:event_btnFinalizarActionPerformed

