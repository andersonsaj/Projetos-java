    public void listar() {
        ProdutoDAO dao = new ProdutoDAO();
        java.util.List<Produto> lista = dao.listarProduto();
        DefaultTableModel dados = (DefaultTableModel) tblEstoque.getModel();
        dados.setNumRows(0);
        for (Produto p : lista) {
            dados.addRow(new Object[]{
                p.getId(),
                p.getDescricao(),
                p.getPreco(),
                p.getQtdEstoque(),
                p.getFornecedor().getNome()
            });

        }
    }

