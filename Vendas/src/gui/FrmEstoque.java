    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        try {
              String nome = "%" + txtDescric.getText() + "%";
        ProdutoDAO dao = new ProdutoDAO();
        java.util.List<Produto> lista = dao.buscaProdutoPorNome(nome);
        DefaultTableModel dados = (DefaultTableModel) tblEstoque.getModel();
        dados.setNumRows(0);
        for (Produto p : lista) {
            dados.addRow(new Object[]{
                p.getId(),
                p.getDescricao(),
                p.getPreco(),
                p.getQtdEstoque(),
                p.getFornecedor().getNome()
            });

        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro inesperado" + e);
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

